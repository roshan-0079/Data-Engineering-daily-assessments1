s = 'Roshan'

print(s.upper())  # converts the string into uppercase
print(s.lower())  # converts the string into lowercase
print(s.isalpha())  # returns true is there is an alphabet
print(s.isdigit())  # returns true is there is a digit
print(s.capitalize())  # capitalizes the first letter
print(s.casefold())  # converts the first letter to lowercase
print(s.isascii())  # returns true if it has ascii values
print(s.isdecimal())  # returns true if it has decimal value
print(s.title())  # converts the string as a title
print(s.split('o'))  # splits the string accordingly
